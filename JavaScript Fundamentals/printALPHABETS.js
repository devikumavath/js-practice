// to print capital letters
for (let i = 65; i <= 90; i++) {
  console.log(String.fromCharCode(i));
}

// to print small letters
for (let i = 97; i <= 122; i++) {
  console.log(String.fromCharCode(i));
}

// to print all ascii  letters
for (let i = 0; i <= 127; i++) {
  console.log(String.fromCharCode(i));
}
